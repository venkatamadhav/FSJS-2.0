let sentence ="     This sentence is to demonstrate how to use trim function to remove unneccessary spaces in a text                    "

// In the below console log , it removes spaces from either ends.
console.log(sentence.trim());

// In the below console log , it removes spaces from start end.
console.log(sentence.trimStart());
// In the below console log , it removes spaces from last end.
console.log(sentence.trimEnd());